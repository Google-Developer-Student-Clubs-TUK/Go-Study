package main

import "fmt"

func main() {

	var a = "initial"
	fmt.Println(a)

	var b, c int = 1, 2
	fmt.Println(b, c)

	var d = true
	fmt.Println(d)

	var e int
	fmt.Println(e)

	var g bool
	fmt.Println(g)

	var h string
	h = "DSfd"
	fmt.Println(h)

	i := "adsad"
	fmt.Println(i)

	f := "apple"
	fmt.Println(f)
}
